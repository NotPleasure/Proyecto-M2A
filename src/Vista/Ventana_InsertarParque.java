    private void SuperficieKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_SuperficieKeyTyped
        // TODO add your handling code here:
          char c = evt.getKeyChar();

        // Permitir dígitos
        if (Character.isDigit(c)) {
            return;
        }

        // Permitir un solo punto decimal, y no como primer carácter
        if (c == '.') {
            String texto = Superficie.getText();
            if (texto.contains(".") || texto.isEmpty()) {
                evt.consume(); // Ignora si ya hay un punto o si es el primer carácter
            }
            return;
        }
        // Ignorar cualquier otro carácter
        evt.consume();
    }//GEN-LAST:event_SuperficieKeyTyped

    private void LatitudKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_LatitudKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        String texto = Latitud.getText();

        // Permitir dígitos
        if (Character.isDigit(c)) {
            return;
        }

        // Permitir un solo punto decimal (y no al inicio)
        if (c == '.') {
            if (texto.contains(".") || texto.isEmpty() || (texto.equals("-"))) {
                evt.consume(); // Ignora si ya hay un punto, si es el primer carácter, o si solo hay el signo negativo
            }
            return;
        }

        // Permitir signo negativo solo como primer carácter
        if (c == '-') {
            if (!texto.isEmpty()) {
                evt.consume(); // Ignora si no es el primer carácter
            }
            return;
        }

        // Ignorar cualquier otro carácter
        evt.consume();
    }//GEN-LAST:event_LatitudKeyTyped


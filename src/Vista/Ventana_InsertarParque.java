    private void CargarImagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CargarImagenActionPerformed

        byte[] datos = null;

        // Intentamos cargar en la primera ranura libre
        if (imagenBytes1 == null) {
            datos = ImagenUtils.seleccionarYConvertirImagen(roundedImageLabel1);
            imagenBytes1 = datos;
        } else if (imagenBytes2 == null) {
            datos = ImagenUtils.seleccionarYConvertirImagen(roundedImageLabel2);
            imagenBytes2 = datos;
        } else if (imagenBytes3 == null) {
            datos = ImagenUtils.seleccionarYConvertirImagen(roundedImageLabel3);
            imagenBytes3 = datos;
        } else {
            JOptionPane.showMessageDialog(this,
                    "Ya cargaste las 3 imágenes.",
                    "Carga de imagen",
                    JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        if (datos == null) {
            JOptionPane.showMessageDialog(this,
                    "No se cargó ninguna imagen.",
                    "Carga de imagen",
                    JOptionPane.WARNING_MESSAGE);

            if (imagenBytes1 == null) {
                roundedImageLabel1.setImagenDesdeBytes(null);
            } else if (imagenBytes2 == null) {
                roundedImageLabel2.setImagenDesdeBytes(null);
            } else if (imagenBytes3 == null) {
                roundedImageLabel3.setImagenDesdeBytes(null);
            }
        }


    }//GEN-LAST:event_CargarImagenActionPerformed

